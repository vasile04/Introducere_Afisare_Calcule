#De la tastatură se întroduce numărul de rînd al culorii curcubeului. De afişat denumirea culorii. Convenim că culoarea roşie are numărul de rînd 1.
n=int(input('Introduceți numărul culorii:'))
print('Ați introdus culoarea cu numărul:', n )
if n==1:
    print('roșu')
if n==2:
    print('oranj')
if n==3:
    print('galben')
if n==4:
    print('verde')
if n==5:
    print('albastru')
if n==6:
    print('indigo')
if n==7:
    print('violet')